import csv
import os

def create_timetable(timetable_str: str, dir: str):
    os.makedirs(dir, exist_ok=True)
    with open(f"{dir}/timetable.csv", "w", newline="") as f:
        writer = csv.writer(f)
        for x in timetable_str.split("/"):
            if x.strip():
                writer.writerow([x.strip()])

